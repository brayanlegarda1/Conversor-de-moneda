    public void habilitarBoton_f(){
        if(!txtLongitud.getText().isEmpty()){
            btnConvertir.setEnabled(true);
        }else{
            btnConvertir.setEnabled(false);
        }
    }
    
    public String ObtnerTipoopcion_f(String opc){
    
        String opcion;
        switch (opc) {
            
            case "Pulgadas a milimetros" -> opcion = "1";
            case "Yardas a metros" -> opcion = "2";
            case "Millas a kilometros" -> opcion = "3";
            default -> throw new AssertionError();
        }
         return opcion;
    }

    private void btnConvertirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConvertirActionPerformed
       
        double quantity = Double.parseDouble(txtLongitud.getText());
        double milimetros=0;
        double metros=0;
        double kilometros=0;
        
        String opcion = jComboBoxMedidas.getSelectedItem().toString();
        
        opcion = ObtnerTipoopcion_f(opcion);
        
        switch (opcion) {
            case "1" -> {
                milimetros = quantity * 25.4;
                txtAreaResultado.setText(quantity +" Pulgadas equivalen a " + milimetros+ " milimetros");
            }
                
            case "2" -> {
                metros = quantity * 0.9144;
                txtAreaResultado.setText(quantity +" Yardas equivalen a " + metros+ " metros");
            }
               
            case "3" -> {
                kilometros = quantity * 1.6093;
                txtAreaResultado.setText(quantity +" Millas equivalen a " + kilometros+ " Kilometros");
            }
                
            default -> throw new AssertionError();
        } 
    }//GEN-LAST:event_btnConvertirActionPerformed

