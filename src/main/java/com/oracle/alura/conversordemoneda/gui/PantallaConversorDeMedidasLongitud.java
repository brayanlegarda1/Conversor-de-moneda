    private void txtLongitudKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtLongitudKeyReleased
          if(!"".equals(txtLongitud.getText())){
            txtAreaResultado.setText("");
        
        }
        
        habilitarBoton_f();
    }//GEN-LAST:event_txtLongitudKeyReleased

    private void txtLongitudKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtLongitudKeyTyped
        
        int key = evt.getKeyChar();

        boolean numeros = key >= 48 && key <= 57;
        boolean borrrar = key == 8;
        boolean punto = key == 46;

        if (!numeros && !borrrar && !punto) {
            evt.consume();
            txtAdvertencia.setText("Solo se permiten Números");
        } else {
            txtAdvertencia.setText("");  
        }
    }//GEN-LAST:event_txtLongitudKeyTyped

